//----------------------------------------------------------------------
// FontMetricsGFX.h
// ================
// Sizes in pixels of ArduinoGFX fonts
// The Arduino fonts are rendered at 141 dpi
// LINE_DEPTH is the spacing between lines - ascent + descent + leading
// CHAR_DEPTH is the ascent + descent, but no leading
// DESCENDER is just the descent
//----------------------------------------------------------------------

#ifndef _INC_FONTMETRICSGFX
#define _INC_FONTMETRICSGFX

#define LINE_DEPTH_9PT    22
#define CHAR_DEPTH_9PT    18
#define DESCENDER_9PT      5

#define LINE_DEPTH_12PT   29
#define CHAR_DEPTH_12PT   23
#define DESCENDER_12PT     6

#define LINE_DEPTH_18PT   42
#define CHAR_DEPTH_18PT   35
#define DESCENDER_18PT     9

#define LINE_DEPTH_24PT   56
#define CHAR_DEPTH_24PT   45
#define DESCENDER_24PT    11

#endif // _INC_FONTMETRICSGFX
